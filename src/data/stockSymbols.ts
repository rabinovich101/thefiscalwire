// Helper function to search stocks
export function searchStocks(query: string): StockSymbol[] {
  if (!query || query.length < 1) return [];

  const lowerQuery = query.toLowerCase();

  return stockSymbols
    .filter(
      (stock) =>
        stock.symbol.toLowerCase().startsWith(lowerQuery) ||
        stock.name.toLowerCase().includes(lowerQuery)
    )
    .sort((a, b) => {
      // Prioritize exact symbol matches
      const aStartsWithSymbol = a.symbol.toLowerCase().startsWith(lowerQuery);
      const bStartsWithSymbol = b.symbol.toLowerCase().startsWith(lowerQuery);

      if (aStartsWithSymbol && !bStartsWithSymbol) return -1;
      if (!aStartsWithSymbol && bStartsWithSymbol) return 1;

      // Then sort alphabetically by symbol
      return a.symbol.localeCompare(b.symbol);
    });
}
